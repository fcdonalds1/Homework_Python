#Булевые значения - True or False
x = int(True)
print(x)

y = int(False)
print(y)

a = []
b = [1, 2, 3]
x = bool(a)
print(x)

y = bool(b)
print(y)

#Операторы сравнения
print(2>3)
print(3<2)
print(2>1)

#Больше либо равно
print(2>=2)
print(3>=2)

#Оператор не равно
print(1!=1)
print(2!=1)

#Сравнение
print(1==1)
print(2==1)

#Сравнение строк
print('text' == 'text')
print('text' == 'text2')
print('text' == 'TEXT')

#Операторы сравнения в цепочках
print(2<3<4<5)
print(2<3<4>5)

#Логическое 'и' результат True - если вся цепочка верна

print(1<2 and 2>1)

#Логический оператор 'или' достаточно, чтобы одна часть была 'True'
print(1>2 or 2<3)

